#Operadores de Atribuição
# Operadores: '+=', '-=', '*=', '/=', '//=', '%=', '**=' 


# Atribuição Simples
saldo = 500
print(saldo)

# Atribuição de adição
saldo = 500
saldo += 200
print(saldo)

# Atribuição de subtração
saldo = 500
saldo -= 100
print(saldo)

# Atribuição de multiplicação
saldo = 500
saldo *= 2
print(saldo)

# Atribuição de divisão
saldo = 500
saldo /= 5
print(saldo)

# Atribuição de divisão inteiro
saldo = 500
saldo //= 5
print(saldo)

# Atribuição com módulo
saldo = 500
saldo %= 480
print(saldo)

# Atribuição de exponenciação
saldo = 5
saldo **= 2
print(saldo)